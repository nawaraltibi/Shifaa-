enum MessageStatus {
  sending, // ⏳ جاري الإرسال (يظهر loading indicator)
  sent, // ✅ تم الإرسال بنجاح (يظهر علامة صح واحدة)
  failed, // ❗️ فشل الإرسال (يظهر علامة تعجب وزر إعادة المحاولة)
  received, // (اختياري) تم الاستلام من الطرف الآخر (علامتي صح)
  read, // (اختياري) تمت القراءة (علامتي صح زرقاء)
}
